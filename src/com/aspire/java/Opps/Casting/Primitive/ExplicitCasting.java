package com.aspire.java.Opps.Casting.Primitive;

public class ExplicitCasting 
{
public static void main(String[] args) 
 {
	double p=1564525.134154;
	float q;
	long r;
	int s;
	short t;
	byte u;
	q=(float) p; // p is type cast in float
	r=(long) q;  // q is type cast in long
	s=(int) r;    // r is type cast in int
	t=(short) s;   // s is type cast in short
	u=(byte) t;    // t is type cast in byte
	 System.out.println(" double p="+p);
	 System.out.println(" float q="+q);
	 System.out.println(" long r="+r);
	 System.out.println(" int s="+s);
	 System.out.println(" short t="+t);
	 System.out.println(" byte u="+u);
	
	
	
	
	
	
	
	
	
 }
	
	
	
	
	
	
	
	
	
	
	
}
