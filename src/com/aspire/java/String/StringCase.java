package com.aspire.java.String;
// program for converting the upper and lower cases
public class StringCase 
{
	public static void main(String[] args) 
	{
		 String s1= "Aspire Training Institute";
		 String s2 ="aspire";
		 String s3="PUNE";
		 String s4="";
	System.out.println(s1.toLowerCase());// to convert lower case i.e.in small letter
	System.out.println(s1.toUpperCase()); // to convert upper case i.e. Capital letter
	System.out.println(s2.toUpperCase()); // to convert upper case i.e. Capital letter
	System.out.println(s3.toLowerCase()); //to convert lower case i.e.in small letter
    System.out.println(s1.length()); // Lenght of string or count of character 
	System.out.println(s2.length()); // Lenght of string or count of character
	System.out.println(s3.length()); // Lenght of string or count of character
	System.out.println(s4.length()); // Lenght of string or count of character
	
	}
 
 
 
 
 
 
 
 
 
 
 
 
}
