package LogicalProgramForInterview;
 // Swipwe number ......>BY CONSIDERING THR THIRD VARIABLE
// a=20;............>a=10;
// b=10;............>b=20;


public class TwoSwipeNumber 
{
public static void main(String[] args) 
{
	int a=20;
	int b=10;
	int temp;
    temp=a;         //........>temp=20;
    a=b;              //.........>a=10;
    b=temp;           //..........>b=20;
    System.out.println("a="+a);
    System.out.println("b="+b);
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
}
